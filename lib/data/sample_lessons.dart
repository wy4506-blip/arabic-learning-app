Lesson _lesson({
  required String id,
  required int sequence,
  required String unitId,
  required String titleCn,
  required String titleAr,
  required int estimatedMinutes,
  required List<String> objectives,
  required List<LessonWord> vocabulary,
  required List<LessonPattern> patterns,
  required List<DialogueLine> dialogues,
  required String grammarTitle,
  required String grammarExplanation,
  required List<LessonExercise> exercises,
  required bool isLocked,
}) {
  return Lesson(
    id: id,
    sequence: sequence,
    unitId: unitId,
    titleCn: titleCn,
    titleAr: titleAr,
    category: 'dialogue',
    difficulty: 1,
    estimatedMinutes: estimatedMinutes,
    objectives: objectives,
    letters: const <String>[],
    vocabulary: vocabulary,
    patterns: patterns,
    dialogues: dialogues,
    grammarTitle: grammarTitle,
    grammarExplanation: grammarExplanation,
    exercises: exercises,
    isLocked: isLocked,
  );
}

LessonWord _word({
  String? id,
  required String ar,
  String? plain,
  required String translit,
  required String zh,
  required String type,
  String? gender,
  String? number,
  String? pluralAr,
  String? pluralPlain,
  String? femAr,
  String? femPlain,
  String? mascAr,
  String? mascPlain,
  String? morphology,
  String? note,
  String? exampleAr,
  String? examplePlain,
  String? exampleZh,
}) {
  return LessonWord(
    id: id,
    arabic: ar,
    plainArabic: plain ?? ar,
    transliteration: translit,
    chinese: zh,
    wordType: type,
    gender: gender,
    number: number,
    pluralFormVocalized: pluralAr,
    pluralFormPlain: pluralPlain,
    feminineFormVocalized: femAr,
    feminineFormPlain: femPlain,
    masculineFormVocalized: mascAr,
    masculineFormPlain: mascPlain,
    morphology: morphology,
    patternNote: note,
    exampleSentenceVocalized: exampleAr,
    exampleSentencePlain: examplePlain,
    exampleTranslationZh: exampleZh,
  );
}

LessonPattern _pattern(String ar, String translit, String zh) {
  return LessonPattern(
    arabic: ar,
    transliteration: translit,
    chinese: zh,
  );
}

DialogueLine _dialogue(
  String speaker,
  String ar,
  String translit,
  String zh,
) {
  return DialogueLine(
    speaker: speaker,
    arabic: ar,
    transliteration: translit,
    chinese: zh,
  );
}

LessonExercise _exercise(
  String question,
  List<String> options,
  String correctAnswer,
) {
  return LessonExercise(
    question: question,
    options: options,
    correctAnswer: correctAnswer,
  );
}

