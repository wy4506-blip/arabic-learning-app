List<AlphabetPronunciationItem> buildAlphabetPronunciations(String letter) {
  return kAlphabetPronunciationStandards
      .map(
        (standard) => AlphabetPronunciationItem(
          id: standard.id,
          key: standard.key,
          shortTitle: standard.shortTitle,
          fullTitle: standard.fullTitle,
          arabicSymbol: standard.arabicSymbol,
          transliteration: standard.transliteration,
          ipa: standard.ipa,
          pronunciationValue: standard.pronunciationValue,
          shortSubtitle: standard.shortSubtitle,
          detailDescription: standard.detailDescription,
          examplePattern: standard.examplePattern,
          audioKey: standard.audioKey,
          sortOrder: standard.sortOrder,
          form: standard.resolveForm(letter),
          audioQueryText: standard.resolveAudioQueryText(letter),
        ),
      )
      .toList(growable: false);
}
