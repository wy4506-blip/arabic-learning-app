import '../models/alphabet_group.dart';

class AlphabetPronunciationStandard {
  final String id;
  final String key;
  final String shortTitle;
  final String fullTitle;
  final String arabicSymbol;
  final String transliteration;
  final String ipa;
  final String pronunciationValue;
  final String shortSubtitle;
  final String detailDescription;
  final String examplePattern;
  final String audioKey;
  final int sortOrder;
  final String? audioQueryPattern;
  final Map<String, String> formOverrides;
  final Map<String, String> audioQueryOverrides;

  const AlphabetPronunciationStandard({
    required this.id,
    required this.key,
    required this.shortTitle,
    required this.fullTitle,
    required this.arabicSymbol,
    required this.transliteration,
    required this.ipa,
    required this.pronunciationValue,
    required this.shortSubtitle,
    required this.detailDescription,
    required this.examplePattern,
    required this.audioKey,
    required this.sortOrder,
    this.audioQueryPattern,
    this.formOverrides = const <String, String>{},
    this.audioQueryOverrides = const <String, String>{},
  });

  String resolveForm(String letter) {
    return formOverrides[letter] ??
        examplePattern.replaceAll('{letter}', letter);
  }

  String resolveAudioQueryText(String letter) {
    return audioQueryOverrides[letter] ??
        (audioQueryPattern ?? examplePattern).replaceAll('{letter}', letter);
  }
}

